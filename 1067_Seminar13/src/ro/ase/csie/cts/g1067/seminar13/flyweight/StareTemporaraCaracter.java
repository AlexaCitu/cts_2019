package ro.ase.csie.cts.g1067.seminar13.flyweight;

public class StareTemporaraCaracter {
	String stare;
	int x;
	int y;
	
	
	public StareTemporaraCaracter(String stare, int x, int y) {
		super();
		this.stare = stare;
		this.x = x;
		this.y = y;
	}


	public String getStare() {
		return stare;
	}


	public void setStare(String stare) {
		this.stare = stare;
	}


	public int getX() {
		return x;
	}


	public void setX(int x) {
		this.x = x;
	}


	public int getY() {
		return y;
	}


	public void setY(int y) {
		this.y = y;
	}
	
	
}
